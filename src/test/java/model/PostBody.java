package model;

public class PostBody {
    private int userId;
    private String id;
    private String title;
    private String body;

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setBody(String body) {
        this.body = body;
    }
}
